    private void btDangKyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDangKyActionPerformed
        Connection con = ConnectDB.getConnectDB();
        int indexCbLop = cbLop.getSelectedIndex();
        int indexCbNhom = cbNhom.getSelectedIndex();
        int indexCbTuan = cbtuan.getSelectedIndex();
        int indexCbThu = cbthu.getSelectedIndex();
        int indexCbCa = cbCa.getSelectedIndex();
        try {

            Statement stmt = con.createStatement();
            PreparedStatement ps = con.prepareStatement("select * from LICH_PHONG where tuan=? and thu=? and maPhong=? and ca=?");
            ps.setString(1, cbtuan.getItemAt(indexCbTuan));
            ps.setString(2, cbthu.getItemAt(indexCbThu));
            ps.setString(3, txtMaPhong.getText());
            ps.setString(4, cbCa.getItemAt(indexCbCa));
            ps.executeQuery();
            if (ps.executeQuery().next()) {
                JOptionPane.showMessageDialog(rootPane, " \"---Phòng học đã đăng ký!!---Vui lòng chọn thời gian và địa điểm học khác!!\"");

            } else if ((Integer.parseInt(txtSLSV.getText()) > 40 && indexCbNhom == 1) || (Integer.parseInt(txtSLSV.getText()) < 40 && indexCbNhom == 2)) {
                JOptionPane.showMessageDialog(rootPane, "Chọn nhóm phù hợp");
            } else {
                System.out.println(indexCbNhom);
                int i = stmt.executeUpdate("INSERT INTO LICH_PHONG(nhom,maLop,tuan,thu,maPhong,ca) VALUES ( '"
                        + cbNhom.getItemAt(indexCbNhom) + "','"
                        + cbLop.getItemAt(indexCbLop) + "','"
                        + cbtuan.getItemAt(indexCbTuan) + "','" + cbthu.getItemAt(indexCbThu) + "','"
                        + txtMaPhong.getText() + "','" + cbCa.getItemAt(indexCbCa) + "')");
                if (i > 0) {
                    JOptionPane.showMessageDialog(rootPane, "Thêm mới thành công!");
                    loadTable();
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng kiểm tra lại thông tin");
        }
    }//GEN-LAST:event_btDangKyActionPerformed

