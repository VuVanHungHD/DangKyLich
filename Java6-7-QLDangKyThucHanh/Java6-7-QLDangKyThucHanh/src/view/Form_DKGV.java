    public void loadDataCB(String sql, JComboBox cb, String bien) {
        Connection con = ConnectDB.getConnectDB();
        try {
            PreparedStatement pts = con.prepareStatement(sql);
            ResultSet rs = pts.executeQuery();
            while (rs.next()) {
                cb.addItem(rs.getString(bien));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadCbTxt(String sql, JComboBox cb, JTextField lb, String bien) {
        Connection con = ConnectDB.getConnectDB();
        try {
            PreparedStatement ptsm = con.prepareStatement(sql);
            ptsm.setString(1, (String) cb.getSelectedItem());
            ResultSet rs = ptsm.executeQuery();
            lb.enable(false);
            while (rs.next()) {
                lb.setText(rs.getString(bien));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

